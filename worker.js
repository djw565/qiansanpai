// 自动判断是否为概念类问题
function isConceptQuestion(q) {
  const patterns = [
    /^什么是/, /是什么意思/, /怎么理解/, /什么叫/,
    /解释一下/, /讲讲/, /说说.*概念/, /.*的定义/,
    /^如何.*分析/, /^怎么用.*分析/,
  ];
  // 短问题 + 没有"我" → 大概率是概念问
  const isShortAndImpersonal = q.length < 30 && !/我/.test(q);
  return patterns.some(p => p.test(q)) || isShortAndImpersonal;
}
